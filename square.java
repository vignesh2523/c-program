import java.util.Scanner;
class square
{
	public static void main(String[] args)
	{
		Scanner sc=new Scanner(System.in);
		int n=sc.nextInt();
		int[] a=new int[n];
		int b=1;
		int count=0;
		for(i=0;i<n;i++)
		{
			a[i]=sc.nextInt();
		}
		if(b*b=a[i])
		{
			count++;
		}
		System.out.printf(+count);
	}
}